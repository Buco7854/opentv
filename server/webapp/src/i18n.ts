const catalogs: Record<'en' | 'fr', Catalog> = { en, fr };

export const languageSetting = {
  get(): Language {
    const v = localStorage.getItem('language');
    return v === 'en' || v === 'fr' ? v : 'auto';
  },
  set(v: Language) {
    if (v === 'auto') localStorage.removeItem('language');
    else localStorage.setItem('language', v);
    notify();
  },
};

function resolve(setting: Language): 'en' | 'fr' {
  if (setting !== 'auto') return setting;
  return (navigator.language || 'en').toLowerCase().startsWith('fr') ? 'fr' : 'en';
}

let listeners: (() => void)[] = [];
const notify = () => listeners.forEach((l) => l());

/** Subscribe/read pair for useSyncExternalStore. */
export const localeStore = {
  subscribe(listener: () => void) {
    listeners.push(listener);
    return () => { listeners = listeners.filter((l) => l !== listener); };
  },
  get: (): 'en' | 'fr' => resolve(languageSetting.get()),
};

/** BCP-47 locale for date/number formatting. */
export const getLocale = () => localeStore.get();

export function t(key: MessageKey, params?: Record<string, string | number>): string {
  let message: string = catalogs[localeStore.get()][key] ?? en[key];
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      message = message.split(`{${name}}`).join(String(value));
    }
  }
  return message;
}
